a=int(input("Nhập a:"))
b=int(input("Nhập b:"))
print("Tổng của a và b:",a+b)
print("Hiệu của a và b:",a-b)
print("Tích của a và b:",a*b)
print("Thương của a và b:",round(a/b,3))    
print("Chia lấy phần dư của a và b:",a%b)
print("Chia lấy phần nguyên của a và b:",a//b)
